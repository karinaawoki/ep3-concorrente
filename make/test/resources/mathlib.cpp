/* Libraries */
#include "mathlib.hpp"

int Math::add(int lhs, int rhs) 
{
    return lhs + rhs;
}

int Math::sub(int lhs, int rhs) 
{
    return lhs - rhs;
}

int Math::mul(int lhs, int rhs) 
{
    return lhs * rhs;
}

int Math::div(int lhs, int rhs) 
{
    return lhs / rhs;
}

int Math::mod(int lhs, int rhs) 
{
    return lhs % rhs;
}
